#!/usr/bin/python3
# future_age_calculator.py
# Calculates how old a user will be in the year 2050

# Get user's current age
current_age = int(input("How old are you? "))

# Calculate future age (2050 - 2023 = 27 years difference)
future_age = current_age + 27

# Print the result
print(f"In 2050, you will be {future_age} years old.")
